     Name
	  cd - Changes working directory.
 
     Syntax
	  cd [directory]
 
     Description
	  If specified,	directory becomes the new working directory;
	  otherwise the	value /system/system/ is used.  The process
    	  must have search (execute) permission in all directories
    	  (components) specified in the full pathname of directory.
 
     See Also
	  pwd, sh, chdir
 
