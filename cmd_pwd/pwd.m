 
 
     PWD(C)		       UNIX System V			PWD(C)
 
 
     Name
	  pwd -	Prints working directory name.
 
     Syntax
	  pwd
 
     Description
	  pwd prints the pathname of the working (current) directory.
 
     See Also
	  cd(C)
 
     Diagnostics
	  ``Cannot open	..'' and ``Read	error in ..'' indicate
	  possible file	system trouble.	 In such cases,	see the	XENIX
	  System Administrator's Guide for information on fixing the
	  file system.
 
 
